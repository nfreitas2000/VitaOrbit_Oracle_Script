CREATE TABLE VITA_USERS (
    USER_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FULL_NAME VARCHAR2(150) NOT NULL,
    EMAIL VARCHAR2(150) NOT NULL,
    PASSWORD VARCHAR2(255) NOT NULL,
    BIRTH_DATE DATE NOT NULL,
    GENDER VARCHAR2(50),
    USER_DESCRIPTION VARCHAR2(500),
    CURRENT_LOCATION VARCHAR2(150),
    PHONE_NUMBER VARCHAR2(30),
    EMERGENCY_CONTACT VARCHAR2(30),
    REGISTERED_AT TIMESTAMP NOT NULL
);
/

CREATE UNIQUE INDEX UK_VITA_USERS_EMAIL ON VITA_USERS (EMAIL);
/

CREATE TABLE VITA_HEALTH_RECORDS (
    HEALTH_RECORD_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USER_ID NUMBER NOT NULL,
    HEART_RATE NUMBER NOT NULL,
    SYSTOLIC_PRESSURE NUMBER NOT NULL,
    DIASTOLIC_PRESSURE NUMBER NOT NULL,
    BODY_TEMPERATURE NUMBER(5,2),
    OXYGEN_SATURATION NUMBER NOT NULL,
    HYDRATION_LEVEL NUMBER(5,2),
    SLEEP_HOURS NUMBER(5,2),
    NOTES VARCHAR2(500),
    RISK_CLASSIFICATION VARCHAR2(50),
    REGISTERED_AT TIMESTAMP NOT NULL,
    CONSTRAINT FK_HEALTH_RECORD_USER
        FOREIGN KEY (USER_ID)
        REFERENCES VITA_USERS(USER_ID)
);
/

CREATE TABLE VITA_SYMPTOM_RECORDS (
    SYMPTOM_RECORD_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USER_ID NUMBER NOT NULL,
    SYMPTOM_NAME VARCHAR2(100) NOT NULL,
    INTENSITY NUMBER NOT NULL,
    FREQUENCY VARCHAR2(50) NOT NULL,
    DESCRIPTION VARCHAR2(500),
    STARTED_AT TIMESTAMP NOT NULL,
    RISK_CLASSIFICATION VARCHAR2(50),
    REGISTERED_AT TIMESTAMP NOT NULL,
    CONSTRAINT FK_SYMPTOM_RECORD_USER
        FOREIGN KEY (USER_ID)
        REFERENCES VITA_USERS(USER_ID)
);
/

CREATE TABLE VITA_ENVIRONMENTAL_CONDITIONS (
    ENVIRONMENTAL_CONDITION_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USER_ID NUMBER NOT NULL UNIQUE,
    EXTERNAL_TEMPERATURE NUMBER(5,2),
    HUMIDITY NUMBER(5,2),
    ALTITUDE NUMBER(8,2),
    ATMOSPHERIC_PRESSURE NUMBER(8,2),
    AIR_QUALITY VARCHAR2(100),
    RADIATION_LEVEL NUMBER(8,2),
    ENVIRONMENT_TYPE VARCHAR2(100),
    REGISTERED_AT TIMESTAMP NOT NULL,
    CONSTRAINT FK_ENVIRONMENTAL_CONDITION_USER
        FOREIGN KEY (USER_ID)
        REFERENCES VITA_USERS(USER_ID)
);
/

CREATE TABLE VITA_EMERGENCIES (
    EMERGENCY_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USER_ID NUMBER NOT NULL,
    LOCATION VARCHAR2(200) NOT NULL,
    MESSAGE VARCHAR2(500) NOT NULL,
    STATUS VARCHAR2(50) NOT NULL,
    REQUEST_DATE TIMESTAMP NOT NULL,
    CONSTRAINT FK_EMERGENCY_USER
        FOREIGN KEY (USER_ID)
        REFERENCES VITA_USERS(USER_ID)
);
/

CREATE TABLE VITA_ALERTS (
    ALERT_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USER_ID NUMBER NOT NULL,
    HEALTH_RECORD_ID NUMBER,
    SYMPTOM_RECORD_ID NUMBER,
    TYPE_ALERT VARCHAR2(100) NOT NULL,
    MESSAGE VARCHAR2(500) NOT NULL,
    RISK_LEVEL VARCHAR2(50) NOT NULL,
    REGISTERED_AT TIMESTAMP NOT NULL,
    CONSTRAINT FK_ALERT_USER
        FOREIGN KEY (USER_ID)
        REFERENCES VITA_USERS(USER_ID),
    CONSTRAINT FK_ALERT_HEALTH_RECORD
        FOREIGN KEY (HEALTH_RECORD_ID)
        REFERENCES VITA_HEALTH_RECORDS(HEALTH_RECORD_ID),
    CONSTRAINT FK_ALERT_SYMPTOM_RECORD
        FOREIGN KEY (SYMPTOM_RECORD_ID)
        REFERENCES VITA_SYMPTOM_RECORDS(SYMPTOM_RECORD_ID)
);
/

